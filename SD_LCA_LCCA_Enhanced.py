    def generate_interaction_network_visualization(self):
        """
        Generate interactive network visualization of cross-category interactions
        Shows synergies and trade-offs between sustainability categories
        
        Creates a directed graph with:
        - Nodes: 4 sustainability categories
        - Edges: Interaction effects (thickness = magnitude, color = type)
        - Green edges = Synergies, Red edges = Trade-offs
        """
        if not hasattr(self, 'results'):
            messagebox.showwarning(
                'No Data',
                'Please run an assessment first from the Input Parameters tab.'
            )
            return
        
        try:
            import plotly.graph_objects as go
            import numpy as np
            
            # ═══════════════════════════════════════════════════════════════
            # Node positions (circular layout)
            # ═══════════════════════════════════════════════════════════════
            categories = ['Material', 'Environmental', 'Operational', 'Economic']
            n_categories = len(categories)
            angles = np.linspace(0, 2*np.pi, n_categories, endpoint=False)
            
            node_x = np.cos(angles)
            node_y = np.sin(angles)
            
            # ═══════════════════════════════════════════════════════════════
            # Extract interaction effects
            # ═══════════════════════════════════════════════════════════════
            effects = self.results['interaction_effects']
            
            interactions = [
                {'from': 'Material', 'to': 'Environmental', 'value': effects['mat_env_effect'], 'key': 'mat_env_effect'},
                {'from': 'Environmental', 'to': 'Operational', 'value': effects['env_op_effect'], 'key': 'env_op_effect'},
                {'from': 'Operational', 'to': 'Economic', 'value': effects['op_econ_effect'], 'key': 'op_econ_effect'},
                {'from': 'Economic', 'to': 'Material', 'value': effects['econ_mat_effect'], 'key': 'econ_mat_effect'},
                {'from': 'Material', 'to': 'Operational', 'value': effects['mat_op_effect'], 'key': 'mat_op_effect'},
                {'from': 'Environmental', 'to': 'Economic', 'value': effects['env_econ_effect'], 'key': 'env_econ_effect'}
            ]
            
            # ═══════════════════════════════════════════════════════════════
            # Create edges (arrows)
            # ═══════════════════════════════════════════════════════════════
            edge_traces = []
            
            for interaction in interactions:
                from_idx = categories.index(interaction['from'])
                to_idx = categories.index(interaction['to'])
                
                # Start and end positions
                x0, y0 = node_x[from_idx], node_y[from_idx]
                x1, y1 = node_x[to_idx], node_y[to_idx]
                
                # Determine color based on effect type
                effect_value = interaction['value']
                if effect_value > 0:
                    color = 'green'  # Synergy
                    interaction_type = 'Synergy'
                else:
                    color = 'red'    # Trade-off
                    interaction_type = 'Trade-off'
                
                # Line width proportional to absolute effect magnitude
                line_width = abs(effect_value) * 2 + 1
                line_width = min(line_width, 10)  # Cap at 10
                
                # Create arrow edge
                edge_trace = go.Scatter(
                    x=[x0, x1, None],
                    y=[y0, y1, None],
                    mode='lines',
                    line=dict(
                        width=line_width,
                        color=color
                    ),
                    hoverinfo='text',
                    hovertext=f"{interaction['from']} → {interaction['to']}<br>"
                              f"Effect: {effect_value:+.2f} points<br>"
                              f"Type: {interaction_type}",
                    showlegend=False
                )
                edge_traces.append(edge_trace)
                
                # Calculate arrow direction
                dx = x1 - x0
                dy = y1 - y0
                length = np.sqrt(dx**2 + dy**2)
                # Shorten to not overlap with node
                arrow_end_x = x0 + 0.85 * dx
                arrow_end_y = y0 + 0.85 * dy
            
            # ═══════════════════════════════════════════════════════════════
            # Create nodes
            # ═══════════════════════════════════════════════════════════════
            node_colors = ['#3498db', '#2ecc71', '#f39c12', '#e74c3c']  # Blue, Green, Orange, Red
            
            scores_original = [
                self.results['material_score'],
                self.results['environmental_score'],
                self.results['operational_score'],
                self.results['economic_score']
            ]
            
            scores_adjusted = [
                self.results['adjusted_material_score'],
                self.results['adjusted_environmental_score'],
                self.results['adjusted_operational_score'],
                self.results['adjusted_economic_score']
            ]
            
            # Node size proportional to adjusted score
            node_sizes = [score * 0.5 + 20 for score in scores_adjusted]
            
            node_trace = go.Scatter(
                x=node_x,
                y=node_y,
                mode='markers+text',
                marker=dict(
                    size=node_sizes,
                    color=node_colors,
                    line=dict(width=3, color='white')
                ),
                text=categories,
                textposition='middle center',
                textfont=dict(size=12, color='white', family='Arial Black'),
                hoverinfo='text',
                hovertext=[
                    f"<b>{cat}</b><br>"
                    f"Original Score: {orig:.1f}<br>"
                    f"Adjusted Score: {adj:.1f}<br>"
                    f"Change: {adj-orig:+.1f}"
                    for cat, orig, adj in zip(categories, scores_original, scores_adjusted)
                ],
                showlegend=False
            )
            
            # ═══════════════════════════════════════════════════════════════
            # Create figure
            # ═══════════════════════════════════════════════════════════════
            fig = go.Figure(data=edge_traces + [node_trace])
            
            # Add legend manually
            fig.add_trace(go.Scatter(
                x=[None], y=[None],
                mode='lines',
                line=dict(width=4, color='green'),
                name='Synergy (Positive)',
                showlegend=True
            ))
            
            fig.add_trace(go.Scatter(
                x=[None], y=[None],
                mode='lines',
                line=dict(width=4, color='red'),
                name='Trade-off (Negative)',
                showlegend=True
            ))
            
            # Layout configuration
            fig.update_layout(
                title=dict(
                    text='Cross-Category Interaction Network<br><sub>Synergies and Trade-offs Between Sustainability Dimensions</sub>',
                    font=dict(size=18, color='#2c3e50'),
                    x=0.5,
                    xanchor='center'
                ),
                showlegend=True,
                legend=dict(
                    x=0.02,
                    y=0.98,
                    bgcolor='rgba(255,255,255,0.8)',
                    bordercolor='black',
                    borderwidth=1
                ),
                xaxis=dict(
                    showgrid=False,
                    showticklabels=False,
                    zeroline=False,
                    range=[-1.5, 1.5]
                ),
                yaxis=dict(
                    showgrid=False,
                    showticklabels=False,
                    zeroline=False,
                    range=[-1.5, 1.5]
                ),
                plot_bgcolor='#f8f9fa',
                paper_bgcolor='white',
                width=900,
                height=900,
                margin=dict(l=50, r=50, t=100, b=50)
            )
            
            # Add annotations for statistics
            annotation_text = (
                f"<b>Interaction Statistics</b><br>"
                f"Total Synergy: {self.results['total_synergy']:.2f} pts<br>"
                f"Total Trade-off: {self.results['total_tradeoff']:.2f} pts<br>"
                f"Synergy Ratio: {self.results['synergy_ratio']:.2f}"
            )
            
            fig.add_annotation(
                text=annotation_text,
                xref="paper", yref="paper",
                x=0.98, y=0.02,
                xanchor='right', yanchor='bottom',
                showarrow=False,
                bgcolor='rgba(255,255,255,0.9)',
                bordercolor='black',
                borderwidth=1,
                font=dict(size=11)
            )
            
            # Save and display
            html_path = 'interaction_network.html'
            fig.write_html(html_path)
            
            import webbrowser
            webbrowser.open(html_path)
            
            messagebox.showinfo(
                'Success',
                f'Interaction network visualization generated!\n\n'
                f'Synergy Ratio: {self.results["synergy_ratio"]:.2f}\n'
                f'(>1.0 = Synergies dominate, <1.0 = Trade-offs dominate)\n\n'
                f'Interactive visualization opened in your browser.'
            )
            
        except Exception as e:
            messagebox.showerror('Error', f'Failed to generate interaction network: {str(e)}')
            
