    frp_intensity = calculate_intensity_score(frp_mass, REF_FRP)
    glass_intensity = calculate_intensity_score(glass_area, REF_GLASS)

    # Operational Energy & Carbon with Chen 2022 Calibration
    energy_per_pax_km_raw = params['energy_per_pax']
    daily_pax_km = params['daily_pax_km'] * 1000
    carbon_intensity = params['carbon_intensity']
    renewable_share = params['renewable_share']

    energy_per_pax_km_calibrated = apply_chen_calibration(energy_per_pax_km_raw, 'operational_energy')
    annual_operational_energy = energy_per_pax_km_calibrated * daily_pax_km * 365

    effective_carbon_intensity = carbon_intensity * (1 - renewable_share / 100.0)
    raw_carbon_intensity_val = energy_per_pax_km_calibrated * effective_carbon_intensity * 0.800
    calibrated_carbon_intensity = raw_carbon_intensity_val
    annual_co2_operational = calibrated_carbon_intensity * daily_pax_km * 365 / 1000

    steel_recycle_rate = params['steel_recycle'] / 100.0
    aluminum_recycle_rate = params['aluminum_recycle'] / 100.0

    # Embodied Energy
    ee_concrete = concrete_volume * DENSITIES['concrete'] * COEFFICIENTS['concrete']
    ee_steel = steel_mass * COEFFICIENTS['steel']
    ee_aluminum = aluminum_mass * COEFFICIENTS['aluminum']
    ee_wood = wood_volume * DENSITIES['wood'] * COEFFICIENTS['wood']
